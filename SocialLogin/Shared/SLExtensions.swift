import Foundation

extension UIColor {
    
    class func slFacebookThemeColor() -> UIColor {
        return UIColor(red: 59/255, green: 89/255, blue: 152/255, alpha: 1.0)
    }
    
    class func slLinkedInThemeColor() -> UIColor {
        return UIColor(red: 0/255, green: 123/255, blue: 181/255, alpha: 1.0)
    }
    
}